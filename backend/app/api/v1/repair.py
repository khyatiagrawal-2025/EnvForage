"""Repair endpoint — POST /api/v1/repair."""
import logging
from typing import Any

from fastapi import APIRouter, HTTPException
from pydantic import BaseModel, Field

from app.services.repair_service import (
    RepairService,
    RepairTemplateNotFoundError,
)
from app.templates.safety import SafetyViolationError

logger = logging.getLogger(__name__)
router = APIRouter()

# Singleton service
_repair_service = RepairService()


# ── Request / Response schemas ────────────────────────────────────────────────

class RepairRequest(BaseModel):
    """Request body for the repair endpoint."""
    template_id: str = Field(
        ..., description="Repair template ID from AI suggestion (e.g. 'repair_cuda_upgrade')."
    )
    params: dict[str, Any] = Field(
        default_factory=dict,
        description="Template parameters (e.g. target_cuda_version, target_python_version).",
    )


class RepairResponse(BaseModel):
    """Response from the repair endpoint."""
    template_id: str
    filename: str
    content: str
    size_bytes: int
    disclaimer: str = (
        "This repair script was generated by EnvForge. "
        "Review it carefully before executing. "
        "EnvForge is not responsible for system changes."
    )


class RepairTemplateInfo(BaseModel):
    """Info about a single repair template."""
    id: str
    description: str


class RepairTemplateListResponse(BaseModel):
    """Response from the list repair templates endpoint."""
    templates: list[RepairTemplateInfo]


# ── Endpoints ─────────────────────────────────────────────────────────────────

@router.post(
    "/repair",
    response_model=RepairResponse,
    status_code=201,
    summary="Generate a repair script from an AI suggestion",
    description=(
        "Given a repair_template_id (from an AI troubleshoot suggestion), "
        "render the corresponding repair script with the provided parameters. "
        "All output is validated through the SafetyFilter."
    ),
    responses={
        201: {"description": "Repair script generated successfully"},
        400: {"description": "Unknown repair template ID"},
        422: {"description": "Invalid request payload"},
        500: {"description": "Safety violation in rendered script"},
    },
)
async def generate_repair(request: RepairRequest) -> RepairResponse:
    """
    Generate a repair script from a template ID and parameters.

    The template ID is typically provided by the AI troubleshoot response
    in the ``repair_template_id`` field of a SuggestedFix.
    """
    try:
        result = _repair_service.render_repair(
            template_id=request.template_id,
            params=request.params,
        )
        return RepairResponse(**result)

    except RepairTemplateNotFoundError as exc:
        raise HTTPException(
            status_code=400,
            detail={
                "error": "UNKNOWN_TEMPLATE",
                "message": str(exc),
                "template_id": exc.template_id,
                "valid_templates": [
                    t["id"] for t in _repair_service.list_templates()
                ],
            },
        ) from exc

    except SafetyViolationError as exc:
        logger.critical("Repair script safety violation: %s", exc)
        raise HTTPException(
            status_code=500,
            detail={
                "error": "SAFETY_VIOLATION",
                "message": (
                    "The repair script was blocked by the safety filter. "
                    "This incident has been logged."
                ),
            },
        ) from exc


@router.get(
    "/repair/templates",
    response_model=RepairTemplateListResponse,
    summary="List available repair templates",
    description="Returns all repair template IDs and descriptions.",
)
async def list_repair_templates() -> RepairTemplateListResponse:
    """List all available repair script templates."""
    templates = _repair_service.list_templates()
    return RepairTemplateListResponse(
        templates=[RepairTemplateInfo(**t) for t in templates]
    )
